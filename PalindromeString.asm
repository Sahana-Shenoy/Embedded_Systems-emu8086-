;To check if string is palindrome
.MODEL SMALL
.STACK
.DATA
STR DB "madam$"
STRLEN DB 0H
FLAG DB 1H  
.STARTUP
MOV CX, 0
MOV AX,0
MOV SI, OFFSET STR

CHECK1:
CMP [SI],'$'
JZ PALINDROME
INC SI
INC STRLEN
JMP CHECK1

PALINDROME:
MOV DX,DS
MOV ES,DX
MOV SI,OFFSET STR
MOV DI,OFFSET STR
MOV CL,STRLEN
DEC STRLEN
MOV AL,STRLEN
ADD DI,AX

LOOP1:
CMPSB
JNE EXIT1
DEC DI
DEC DI
LOOP LOOP1

EXIT1:
CMP CL,0H
JZ EXIT2
MOV FLAG,0H

EXIT2:
.EXIT
END